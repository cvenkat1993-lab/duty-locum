// ── Helper: pick 5 random questions from a department's pool ─
export function getQuizQuestions(department: string): QuizQuestion[] | null {
  const pool = QUESTION_BANK[department];
  if (!pool || pool.length < 5) return null;

  // Fisher-Yates shuffle, then take first 5
  const shuffled = [...pool];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, 5);
}
